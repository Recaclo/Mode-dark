function darkMode(){
    const body = document.body;
    // meterse al documento html, con ayuda de la palabra reservada y acceder al body y lo guada en una variable
    // DOM documento objet module 
    /*classlist esta en todos los documentos y nos permite acceder y manipular clases en CSS */
    // nombre de la clase dark-mode
    body.classList.toggle("dark-mode");
    

}